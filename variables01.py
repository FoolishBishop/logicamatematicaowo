# 1)
# primero, definimos la variable. Estas pueden tener cualquier nombre con tal que no tengan espacios
variable = "Hola"
# ^^^ notese que se puso comillas entre el valor, esto porque los valores de texto se les debe  poner comillas
# la accion "print()" consiste en mostrar en la consola (tu pantalla), en este caso la variable llamada variable
print(variable)
# >>> Hola

# 2)
# las variables no son constantes, por lo que se pueden actualizar
cantidad_de_manzanas = 46  # <--- aqui no usa comillas porque son valores numericos que podemos sumar, restar, etc
